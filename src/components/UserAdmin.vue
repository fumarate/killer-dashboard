<template>
    <van-pull-refresh v-model="refreshing" @refresh="onRefresh">
        <div style="height:1rem"></div>
        <van-loading v-if="loading" type="spinner" />
        <van-skeleton title row="3" :loading="loading">
            <van-cell-group inset>
                <van-swipe-cell v-for="user in users" :key="user.userId">
                    <van-cell>
                        {{ user.userId }}
                    </van-cell>
                    <template #right>
                        <van-button type="danger" @click="deleteUser(user.userId)">删除</van-button>
                    </template>
                </van-swipe-cell>
            </van-cell-group>
            <div style="margin:16px">
                <van-button type="primary" round @click="addUser" :style="{ width: '100%' }">添加用户</van-button>
            </div>
        </van-skeleton>
    </van-pull-refresh>
</template>
<script>
import { default as api } from '../api/api'
import { Button, Cell, CellGroup, Dialog, Field, SwipeCell, PullRefresh, Loading, Skeleton } from 'vant'
export default {
    components: {
        [Button.name]: Button,
        [Cell.name]: Cell,
        [CellGroup.name]: CellGroup,
        [Dialog.name]: Dialog,
        [Field.name]: Field,
        [SwipeCell.name]: SwipeCell,
        [PullRefresh.name]: PullRefresh,
        [Loading.name]: Loading,
        [Skeleton.name]: Skeleton
    },
    data() {
        return {
            users: null,
            newUserId: "",
            newUserCaptcha: "",
            refreshing: false,
            loading: true
        };
    },
    mounted() {
        this.getUser()
        setTimeout(
            () => {
                this.loading = false;
            },
            500)
    },
    methods: {
        onRefresh() {
            this.getUser();
            setTimeout(() => {
                this.refreshing = false;
            }, 1000);
        },
        getUser() {
            fetch(api + "/user", {
                method: "GET",
            })
                .then((resp) => resp.json())
                .then((respJson) => (this.users = respJson.data))
        },
        addUser: function () {
            this.$router.push("/user/add");
        },
        deleteUser: function (userId) {
            Dialog.confirm({
                "message": "确定删除用户" + userId + "吗？"
            })
                .then(() => {
                    fetch(api + "/user/" + userId, {
                        method: "delete",
                    })
                        .then((resp) => resp.json())
                        .then((respJson) => {
                            if (respJson.status) {
                                Dialog.alert({ message: "删除成功!" });
                                location.reload();
                            } else {
                                Dialog.alert({ message: respJson.exception });
                            }
                        });
                })
        },
    }
}
</script>